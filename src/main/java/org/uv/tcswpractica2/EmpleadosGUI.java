    private void btoGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoGuardarActionPerformed
        DAOEmpleado dao = new DAOEmpleado();
        if (!validarCampos()) return;
        PojoEmpleado pojo = new PojoEmpleado();
        pojo.setClave(Long.parseLong(txtClave.getText()));
        pojo.setNombre(txtNombre.getText());
        pojo.setDireccion(txtDireccion.getText());
        pojo.setTelefono(txtTelefono.getText());
        boolean res;
        if (claveSeleccionada == null) {
            res = dao.guardar(pojo);
            if (res)
                JOptionPane.showMessageDialog(this, "Se guardó correctamente");
            else
                JOptionPane.showMessageDialog(this, "Error al guardar");
        } else {
            res= dao.modificar(pojo, claveSeleccionada) != null;
            
            if (res)
                JOptionPane.showMessageDialog(this, "Se actualizó correctamente");
            else
                JOptionPane.showMessageDialog(this, "Error al actualizar");
        }
        if (res) {
            cargarTabla();
            limpiarCampos();
        }
    



    }//GEN-LAST:event_btoGuardarActionPerformed

    private void btoEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoEliminarActionPerformed
        if (claveSeleccionada == null) {
            JOptionPane.showMessageDialog(this, "Seleccione un empleado para eliminar");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this,
                "¿Está seguro de eliminar este empleado?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            DAOEmpleado dao = new DAOEmpleado();
            PojoEmpleado resultado = dao.eliminar(claveSeleccionada);
            if (resultado != null) {
                JOptionPane.showMessageDialog(this, "Empleado eliminado correctamente");
                cargarTabla();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar el empleado", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    
    }//GEN-LAST:event_btoEliminarActionPerformed


