    public EmpleadosGUI() {
        initComponents();
        initTable();
        jTable1.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
                cargarDatosSeleccionados();
            }
        });
    }
    
    public void initTable() {
        String[] columnas = { COL_CLAVE, COL_NOMBRE, COL_DIRECCION, COL_TELEFONO};
        modeloTabla = new DefaultTableModel(columnas, 0) {
        };
        
        jTable1.setModel(modeloTabla);
        
        cargarTabla();
    }
    
    
    
    private void cargarDatosSeleccionados() {
        int fila = jTable1.getSelectedRow();
        if (fila != -1) {
            claveSeleccionada = Long.parseLong(modeloTabla.getValueAt(fila, 0).toString());
            txtClave.setText(claveSeleccionada.toString());
            txtClave.setEditable(false); 
            txtNombre.setText(modeloTabla.getValueAt(fila, 1).toString());
            txtDireccion.setText(modeloTabla.getValueAt(fila, 2).toString());
            txtTelefono.setText(modeloTabla.getValueAt(fila, 3).toString());
            btoGuardar.setText("Actualizar");
            btoEliminar.setEnabled(true);
        }
    }
    
    
    private void limpiarCampos() {
        txtClave.setText("");
        txtClave.setEditable(true);
        txtNombre.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        claveSeleccionada = null;
        btoGuardar.setText("Guardar");
        btoEliminar.setEnabled(false);
        jTable1.clearSelection();
    }
    
    
    private boolean validarCampos() {
        if (txtClave.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "La clave es obligatoria");
            txtClave.requestFocus();
            return false;
        }
        
        if (txtNombre.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El nombre es obligatorio");
            txtNombre.requestFocus();
            return false;
        }
        
        if (txtDireccion.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "La dirección es obligatoria");
            txtDireccion.requestFocus();
            return false;
        }
        
        if (txtTelefono.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El teléfono es obligatorio");
            txtTelefono.requestFocus();
            return false;
        }
        
        try {
            Integer.parseInt(txtClave.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La clave debe ser un número válido");
            txtClave.requestFocus();
            return false;
        }
        
        return true;
    }
    
    private void cargarTabla() {
        DAOEmpleado dao = new DAOEmpleado();
        
        modeloTabla.setRowCount(0);
        
        try {
            List<PojoEmpleado> empleados = dao.findAll();
            for (PojoEmpleado emp : empleados) {
                Object[] fila = {
                    emp.getClave(),
                    emp.getNombre(),
                    emp.getDireccion(),
                    emp.getTelefono()
                };
                modeloTabla.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos: " + e.getMessage());
        }
    }

